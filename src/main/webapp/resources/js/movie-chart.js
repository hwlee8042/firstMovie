$('#container .ch-bt .prevBt').click(function(){
	$('.movie-chart2').each(function(){
		$('.movie-chart2').show();
		$('.movie-chart2').addClass('move', 300);
		$('.movie-chart2').removeClass('move');
	});
});



$('#container .ch-bt .nextBt').click(function(){
	$('.movie-chart2').each(function(){
		$('.movie-chart2').show();
		$('.movie-chart2').addClass('move', 300);
		$('.movie-chart2').removeClass('move');
	});
});
